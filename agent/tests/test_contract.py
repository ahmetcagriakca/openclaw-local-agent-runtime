class ContractTestBase(unittest.TestCase):
    """Base class for contract tests with shared client and spec."""

    _tmpdir = None

    @classmethod
    def setUpClass(cls):
        import tempfile
        cls._tmpdir = tempfile.mkdtemp()
        tmpdir = Path(cls._tmpdir)

        # Set up minimal server state for integration tests
        missions_dir = tmpdir / "missions"
        missions_dir.mkdir()
        approvals_dir = tmpdir / "approvals"
        approvals_dir.mkdir()

        # Create a test mission file
        (missions_dir / "mission-contract-test.json").write_text(json.dumps({
            "missionId": "contract-test-001",
            "status": "completed",
            "goal": "contract test mission",
            "stages": [{"id": "s1", "specialist": "analyst", "status": "completed"}],
        }), encoding="utf-8")

        caps_path = tmpdir / "capabilities.json"
        caps_path.write_text(json.dumps({
            "autoGenerated": True,
            "capabilities": {"deny_forensics": {"available": True}},
        }), encoding="utf-8")

        import api.server as srv
        srv.MISSIONS_DIR = missions_dir
        srv.APPROVALS_DIR = approvals_dir
        srv.TELEMETRY_PATH = tmpdir / "telemetry.jsonl"
        srv.CAPABILITIES_PATH = caps_path
        srv.SERVICES_PATH = tmpdir / "services.json"

        from api.capabilities import CapabilityChecker
        from api.normalizer import MissionNormalizer
        srv.normalizer = MissionNormalizer(
            missions_dir, srv.TELEMETRY_PATH, caps_path, approvals_dir)
        srv.capability_checker = CapabilityChecker(caps_path)

        cls.client = TestClient(srv.app)
        if SPEC_PATH.exists():
            cls.spec = json.loads(SPEC_PATH.read_text(encoding="utf-8"))
        else:
            cls.spec = {}

    @classmethod
    def tearDownClass(cls):
        import shutil
        if cls._tmpdir:
            shutil.rmtree(cls._tmpdir, ignore_errors=True)

    def assert_meta_present(self, data: dict, endpoint: str):
        """Assert response contains meta field (D-067 wrapper)."""
        self.assertIn("meta", data,
                       f"{endpoint}: Missing 'meta' in response wrapper")
        meta = data["meta"]
        self.assertIn("dataQuality", meta,
                       f"{endpoint}: Missing 'dataQuality' in meta")
        self.assertIn("generatedAt", meta,
                       f"{endpoint}: Missing 'generatedAt' in meta")

    def assert_endpoint_exists_in_spec(self, method: str, path: str):
        """Assert endpoint is documented in OpenAPI spec."""
        if not self.spec:
            self.skipTest("OpenAPI spec not available")
        paths = self.spec.get("paths", {})
        self.assertIn(path, paths,
                       f"Endpoint {path} not found in OpenAPI spec")
        self.assertIn(method, paths[path],
                       f"Method {method} not found for {path} in spec")


class TestMissionsContract(ContractTestBase):
    """Contract tests for /api/v1/missions endpoints."""

    def test_01_list_missions_shape(self):
        """GET /missions returns expected response shape."""
        resp = self.client.get("/api/v1/missions")
        self.assertEqual(resp.status_code, 200)
        data = resp.json()
        self.assert_meta_present(data, "GET /missions")
        self.assertIn("missions", data)
        self.assertIsInstance(data["missions"], list)

    def test_02_list_missions_in_spec(self):
        """GET /missions is documented in OpenAPI spec."""
        self.assert_endpoint_exists_in_spec("get", "/api/v1/missions")

    def test_03_mission_not_found_shape(self):
        """GET /missions/{id} 404 returns RFC 9457 error envelope."""
        resp = self.client.get("/api/v1/missions/nonexistent-id-12345")
        self.assertEqual(resp.status_code, 404)
        data = resp.json()
        self.assertIn("type", data)
        self.assertIn("status", data)
        self.assertIn("detail", data)
        self.assertEqual(data["status"], 404)


