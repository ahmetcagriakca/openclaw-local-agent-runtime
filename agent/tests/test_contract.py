class TestBreakingChangeDetection(unittest.TestCase):
    """Tests for the contract_check.py tool itself."""

    def test_20_extract_endpoints(self):
        """contract_check extracts endpoints from spec."""
        sys.path.insert(0, str(OC_ROOT / "tools"))
        from contract_check import extract_endpoints

        spec = {
            "paths": {
                "/api/v1/test": {
                    "get": {
                        "parameters": [
                            {"name": "q", "in": "query", "required": True}
                        ],
                        "responses": {
                            "200": {
                                "content": {
                                    "application/json": {
                                        "schema": {"$ref": "#/components/schemas/TestResp"}
                                    }
                                }
                            }
                        },
                        "tags": ["test"],
                    }
                }
            }
        }
        eps = extract_endpoints(spec)
        self.assertEqual(len(eps), 1)
        self.assertIn("GET /api/v1/test", eps)
        self.assertEqual(eps["GET /api/v1/test"]["params"][0]["name"], "q")

    def test_21_detect_removed_endpoint(self):
        """contract_check detects removed endpoints."""
        sys.path.insert(0, str(OC_ROOT / "tools"))
        from contract_check import check_breaking_changes

        baseline = {"paths": {"/api/v1/foo": {"get": {"responses": {}}}}}
        current = {"paths": {}}
        issues = check_breaking_changes(baseline, current)
        self.assertTrue(any("removed" in i.lower() for i in issues))

    def test_22_no_breaking_when_identical(self):
        """contract_check reports no issues for identical specs."""
        sys.path.insert(0, str(OC_ROOT / "tools"))
        from contract_check import check_breaking_changes

        spec = {"paths": {"/api/v1/test": {"get": {"responses": {}}}}}
        issues = check_breaking_changes(spec, spec)
        self.assertEqual(len(issues), 0)


